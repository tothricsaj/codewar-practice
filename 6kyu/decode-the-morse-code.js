const MORSE_CODE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
}

decodeMorse = function(morseCode){

    return morseCode.split(' ').map((el, index)=> {
        if(el === '') {
            return ' ';
        }
        if(!!el) {
            return MORSE_CODE[el].toUpperCase();
        }
    }).join('').replace(/  +/g, ' ');
}

console.log(decodeMorse('.... . -.--   .--- ..- -.. .'));
console.log(decodeMorse('.... .   .--- ..-   -.. .'));
